function EnemyCreate() {
         EnemyNumber++;
         Enemy[EnemyNumber]=[];
         Enemy[EnemyNumber][0]= EnemyType[1];
         Enemy[EnemyNumber][1]= EnemyHP[1];
         
         Enemy[EnemyNumber][2]=1+Math.floor(Math.random()*(mapColsRows-2));;
         Enemy[EnemyNumber][3]=1+Math.floor(Math.random()*(mapColsRows-2));;
         mapdata[Enemy[EnemyNumber][2]][Enemy[EnemyNumber][3]][1]=EnemyNumber;
		 mapdata[Enemy[EnemyNumber][2]][Enemy[EnemyNumber][3]][0]=1;
		 OnePointCreate(Enemy[EnemyNumber][2],Enemy[EnemyNumber][3]);
         DrawEnemy(EnemyNumber);
}

function EnemyMove(MoveEnemyNumber) {
		 var move=Math.floor(Math.random()*4);
		 switch(move){
			 
		 case 0:  //右へ移動
		 var move_y=0;
		 var move_x=1;
		 break;
		 
		 case 1:  //下へ移動
		 var move_y=1;
		 var move_x=0;
		 break;
		 
		 case 2:  //左へ移動
		 var move_y=0;
		 var move_x=(-1);
		 break;
		 
		 case 3:  //上へ移動
		 var move_y=(-1);
		 var move_x=0;
		 break;
		 
		 }

         if(mapdata[Enemy[MoveEnemyNumber][2]+move_y][Enemy[MoveEnemyNumber][3]+move_x][0]==1&&mapdata[Enemy[MoveEnemyNumber][2]+move_y][Enemy[MoveEnemyNumber][3]+move_x][1]==0){

         mapdata[Enemy[MoveEnemyNumber][2]][Enemy[MoveEnemyNumber][3]][1]=0;
         OnePointCreate(Enemy[MoveEnemyNumber][2],Enemy[MoveEnemyNumber][3]);
         
		 Enemy[MoveEnemyNumber][2]=Enemy[MoveEnemyNumber][2]+move_y;
         Enemy[MoveEnemyNumber][3]=Enemy[MoveEnemyNumber][3]+move_x;
         mapdata[Enemy[MoveEnemyNumber][2]][Enemy[MoveEnemyNumber][3]][1]=MoveEnemyNumber;
         DrawEnemy(MoveEnemyNumber);
         }
}


